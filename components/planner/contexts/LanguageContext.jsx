const LanguageContext = createContext();

// Provider
export function LanguageProvider({ children }) {
    const [currentLanguage, setCurrentLanguage] = useState('fr');

    // Charger la langue depuis localStorage + écouter les changements de l'app principale
    useEffect(() => {
        const syncLang = () => {
            const saved = localStorage.getItem('preferred-language');
            if (saved && LANGUAGES[saved]) setCurrentLanguage(saved);
        };
        syncLang();
        window.addEventListener('storage', syncLang);
        return () => window.removeEventListener('storage', syncLang);
    }, []);

    // Fonction pour changer de langue
    const changeLanguage = (languageCode) => {
        if (LANGUAGES[languageCode]) {
            setCurrentLanguage(languageCode);
            localStorage.setItem('preferred-language', languageCode);
        }
    };

    // Fonction de traduction
    const t = (key, defaultValue = key) => {
        return translations[currentLanguage]?.[key] || defaultValue;
    };

    // Fonction pour obtenir le nom de la langue actuelle
    const getCurrentLanguageName = () => {
        return LANGUAGES[currentLanguage]?.name || 'Français';
    };

    // Fonction pour obtenir le drapeau de la langue actuelle
    const getCurrentLanguageFlag = () => {
        return LANGUAGES[currentLanguage]?.flag || '🇫🇷';
    };

    const value = {
        currentLanguage,
        changeLanguage,
        t,
        getCurrentLanguageName,
        getCurrentLanguageFlag,
        availableLanguages: Object.values(LANGUAGES)
    };

    return (
        <LanguageContext.Provider value={value}>
            {children}
        </LanguageContext.Provider>
    );
}

// Hook pour utiliser le contexte de langue
export function useLanguage() {
    const context = useContext(LanguageContext);
    if (!context) {
        throw new Error('useLanguage must be used within a LanguageProvider');
    }
    return context;
}

export default LanguageContext;